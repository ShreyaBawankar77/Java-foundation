class Control {
    public static void main(String[] args) {
        int n = 5;
        if(n > 0) System.out.println("Positive");
        else System.out.println("Negative");

        for(int i=0;i<3;i++) System.out.print(i);

        int i=0;
        while(i<3){ System.out.print(i); i++; }
    }
}
